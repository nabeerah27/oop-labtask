package Week13_Mini_LMS.code;

public class Student {

    private String name;
    private String seatNo;

    public Student(String name, String seatNo) {
        this.name = name;
        this.seatNo = seatNo;
    }

    public String getName() {
        return name;
    }

    public String getSeatNo() {
        return seatNo;
    }

}
